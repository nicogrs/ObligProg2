    private void btnComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprarActionPerformed
        Puesto puesto = (Puesto) listaPuesto.getSelectedValue();
        try {
            if (listaPuesto.getSelectedValue() != null && listaProducto.getSelectedValue() != null) {
                puesto.comprarProducto((Producto) listaProducto.getSelectedValue(), (int) spinnerCantidad.getValue());
                modelo.agregarMovimiento((int) spinnerCantidad.getValue(), (int) spinnerPrecio.getValue(), puesto, (Producto) listaProducto.getSelectedValue(), TIPO_MOVIMIENTO);
                JOptionPane.showMessageDialog(this, "Â¡La compra se realizo con exito!",
                        "Informacion del sistema.", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "No fue posible realizar la compra del producto.\nRevise que todos los campos esten completos o sean correctos.",
                        "Informacion del sistema.", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Hubo un error al realizar la compra.\nContacte con sistemas.",
                    "Informacion del sistema.", JOptionPane.INFORMATION_MESSAGE);
        }
        this.btnComprar.setSelected(false);
    }//GEN-LAST:event_btnComprarActionPerformed


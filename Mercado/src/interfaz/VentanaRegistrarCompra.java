    private void listaProductoValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listaProductoValueChanged
        if (listaProducto.getSelectedValue() != null) {
            Producto prod = (Producto) listaProducto.getSelectedValue();
            lblTipoVenta.setText("Tipo venta: " + Utilidades.devolverTipoVentaAMostrar(prod.getFormaVenta(), 1)); // Se setea en 1 para mostrar la forma de venta en singular
        } else {
            lblTipoVenta.setText("Tipo venta: ");
        }
    }//GEN-LAST:event_listaProductoValueChanged

    private void cargar() {
        listaPuesto.setListData(modelo.getListaPuestos().toArray());
        listaMayorista.setListData(modelo.getListaMayoristas().toArray());
    }

    @Override
    public void propertyChange(PropertyChangeEvent evt) {
        cargar();
        if (listaMayorista.getSelectedValue() != null) {
            Mayorista m1 = (Mayorista) listaMayorista.getSelectedValue();
            listaProducto.setListData(m1.getListaProductos().toArray());
        }else{
            DefaultListModel listaModelo = new DefaultListModel();
            listaProducto.setModel(listaModelo);
        }
    }

